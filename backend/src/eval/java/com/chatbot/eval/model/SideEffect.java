package com.chatbot.eval.model;

public class SideEffect {

    private String type;
    private String status;

    public SideEffect() {
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
